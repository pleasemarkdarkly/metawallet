        /// <summary>This method is used to add DataType items.</summary>
        // ------------------------------------------------------------------------------
        public static void PopulateItem(BLL.Core.DataType item, MOD.Data.NamedObjectCollection useCollections, bool performCascade, bool isAddOperation, bool isCollectionOperation, MOD.Test.TestResults results)
        {
            if (item == null)
            {
                return;
            }

            //
            // populate the DataType item
            //
            if (isAddOperation == true)
            {
                item.DataTypeCode = TestHelper.GetInt(null, null);
            }
            item.DataTypeName = TestHelper.GetString(null, 100);
            item.Description = TestHelper.GetString(null, 1000);
            item.IsActive = TestHelper.GetBool();
            PopulateItemCleanup(item, useCollections, performCascade, isAddOperation, isCollectionOperation, results);
        }

        // ------------------------------------------------------------------------------
        /// <summary>This method is used to add DataType items.</summary>
        // ------------------------------------------------------------------------------
        public static BLL.Core.DataType AddOneDataTypeTest(bool performCascade, MOD.Test.TestResults results)
        {
            long startTicks = 0;
            long endTicks = 0;
            //
            // add item
            //
            BLL.Core.DataType item = new BLL.Core.DataType();
            MOD.Data.NamedObjectCollection useCollections = new MOD.Data.NamedObjectCollection();
            BLL.SortableList<BLL.Core.DataType> itemsCoreDataType = new BLL.SortableList<BLL.Core.DataType>();
            itemsCoreDataType.Add((BLL.Core.DataType)item);
            useCollections["Core.DataType"] = itemsCoreDataType;
            DataType_Manager.PopulateItem(item, useCollections, performCascade, true, false, results);
            results.IsCollision = false;
            try
            {
                BLL.Core.DataType item2 = BLL.Core.DataTypeManager.GetOneDataType(item.DataTypeCode);
                if (item2 != null)
                {
                    results.IsCollision = true;
                }
            }
            catch (UnitTestException ex)
            {
                throw ex;
            }
            catch (System.Exception ex)
            {
                throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
            }
            if (results.IsCollision == false)
            {
                try
                {
                    if (IsValidForAdd(item, performCascade, results) == true)
                    {
                        startTicks = DateTime.Now.Ticks;
                        BLL.Core.DataTypeManager.AddOneDataType(item, performCascade);
                        endTicks = DateTime.Now.Ticks;
                        results.AddPerfMetric(TestHelper.GetSecondsFromTicks(endTicks - startTicks), null, String.Empty);
                    }
                }
                catch (UnitTestException ex)
                {
                    throw ex;
                }
                catch (System.Exception ex)
                {
                    throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
                }
            }
            return item;
        }

