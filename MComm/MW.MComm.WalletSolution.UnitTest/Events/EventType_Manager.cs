        /// <summary>This method is used to add EventType items.</summary>
        // ------------------------------------------------------------------------------
        public static void PopulateItem(BLL.Events.EventType item, MOD.Data.NamedObjectCollection useCollections, bool performCascade, bool isAddOperation, bool isCollectionOperation, MOD.Test.TestResults results)
        {
            if (item == null)
            {
                return;
            }

            //
            // populate the EventType item
            //
            if (isAddOperation == true)
            {
                item.EventTypeCode = TestHelper.GetInt(null, null);
            }
            item.EventTypeName = TestHelper.GetString(null, 255);
            item.Description = TestHelper.GetString(null, 2000);
            item.IsActive = TestHelper.GetNullableBool();
            PopulateItemCleanup(item, useCollections, performCascade, isAddOperation, isCollectionOperation, results);
        }

        // ------------------------------------------------------------------------------
        /// <summary>This method is used to add EventType items.</summary>
        // ------------------------------------------------------------------------------
        public static BLL.Events.EventType AddOneEventTypeTest(bool performCascade, MOD.Test.TestResults results)
        {
            long startTicks = 0;
            long endTicks = 0;
            //
            // add item
            //
            BLL.Events.EventType item = new BLL.Events.EventType();
            MOD.Data.NamedObjectCollection useCollections = new MOD.Data.NamedObjectCollection();
            BLL.SortableList<BLL.Events.EventType> itemsEventsEventType = new BLL.SortableList<BLL.Events.EventType>();
            itemsEventsEventType.Add((BLL.Events.EventType)item);
            useCollections["Events.EventType"] = itemsEventsEventType;
            EventType_Manager.PopulateItem(item, useCollections, performCascade, true, false, results);
            results.IsCollision = false;
            try
            {
                BLL.Events.EventType item2 = BLL.Events.EventTypeManager.GetOneEventType(item.EventTypeCode);
                if (item2 != null)
                {
                    results.IsCollision = true;
                }
            }
            catch (UnitTestException ex)
            {
                throw ex;
            }
            catch (System.Exception ex)
            {
                throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
            }
            if (results.IsCollision == false)
            {
                try
                {
                    if (IsValidForAdd(item, performCascade, results) == true)
                    {
                        startTicks = DateTime.Now.Ticks;
                        BLL.Events.EventTypeManager.AddOneEventType(item, performCascade);
                        endTicks = DateTime.Now.Ticks;
                        results.AddPerfMetric(TestHelper.GetSecondsFromTicks(endTicks - startTicks), null, String.Empty);
                    }
                }
                catch (UnitTestException ex)
                {
                    throw ex;
                }
                catch (System.Exception ex)
                {
                    throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
                }
            }
            return item;
        }

