        /// <summary>This method is used to add Error items.</summary>
        // ------------------------------------------------------------------------------
        public static void PopulateItem(BLL.Core.Error item, MOD.Data.NamedObjectCollection useCollections, bool performCascade, bool isAddOperation, bool isCollectionOperation, MOD.Test.TestResults results)
        {
            if (item == null)
            {
                return;
            }

            //
            // populate the Error item
            //
            if (isAddOperation == true)
            {
                item.ErrorNumber = TestHelper.GetInt(null, null);
            }
            item.ErrorTitle = TestHelper.GetString(null, 100);
            item.ErrorMessage = TestHelper.GetString(null, 1000);
            PopulateItemCleanup(item, useCollections, performCascade, isAddOperation, isCollectionOperation, results);
        }

        // ------------------------------------------------------------------------------
        /// <summary>This method is used to add Error items.</summary>
        // ------------------------------------------------------------------------------
        public static BLL.Core.Error AddOneErrorTest(bool performCascade, MOD.Test.TestResults results)
        {
            long startTicks = 0;
            long endTicks = 0;
            //
            // add item
            //
            BLL.Core.Error item = new BLL.Core.Error();
            MOD.Data.NamedObjectCollection useCollections = new MOD.Data.NamedObjectCollection();
            BLL.SortableList<BLL.Core.Error> itemsCoreError = new BLL.SortableList<BLL.Core.Error>();
            itemsCoreError.Add((BLL.Core.Error)item);
            useCollections["Core.Error"] = itemsCoreError;
            Error_Manager.PopulateItem(item, useCollections, performCascade, true, false, results);
            results.IsCollision = false;
            try
            {
                BLL.Core.Error item2 = BLL.Core.ErrorManager.GetOneError(item.ErrorNumber);
                if (item2 != null)
                {
                    results.IsCollision = true;
                }
            }
            catch (UnitTestException ex)
            {
                throw ex;
            }
            catch (System.Exception ex)
            {
                throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
            }
            if (results.IsCollision == false)
            {
                try
                {
                    if (IsValidForAdd(item, performCascade, results) == true)
                    {
                        startTicks = DateTime.Now.Ticks;
                        BLL.Core.ErrorManager.AddOneError(item, performCascade);
                        endTicks = DateTime.Now.Ticks;
                        results.AddPerfMetric(TestHelper.GetSecondsFromTicks(endTicks - startTicks), null, String.Empty);
                    }
                }
                catch (UnitTestException ex)
                {
                    throw ex;
                }
                catch (System.Exception ex)
                {
                    throw new System.Exception(ex.Message + " {primary key: " + item.PrimaryKey + "}", ex);
                }
            }
            return item;
        }

